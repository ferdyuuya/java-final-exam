    private void jTextSearchKeyTyped(java.awt.event.KeyEvent evt) {
        String searchText = jTextSearch.getText().trim();

        TableRowSorter<TableModel> rowSorter = new TableRowSorter<>(jTableBooks.getModel());
        jTableBooks.setRowSorter(rowSorter);

        RowFilter<TableModel, Object> rowFilter = RowFilter.regexFilter("(?i)" + searchText);
        rowSorter.setRowFilter(rowFilter);
    }

    private void jTextSearchFocusLost(java.awt.event.FocusEvent evt) {
        //Mengembalikan teks placeholder jTextSearch ketika tidak ada interaksi
        if(jTextSearch.getText().trim().equals("") || jTextSearch.getText().trim().toLowerCase().equals("search by title/author/publisher/release date/isbn")){
            jTextSearch.setText("Search by Title/Author/Publisher/Release Date/ISBN");
            jTextSearch.setForeground(new java.awt.Color(153,153,153));
        }
    }

    private void jTextSearchFocusGained(java.awt.event.FocusEvent evt) {
        //Menghapus teks placeholder jTextSearch ketika dilakukan interaksi
        if(jTextSearch.getText().trim().toLowerCase().equals("search by title/author/publisher/release date/isbn")){
            jTextSearch.setText("");
            jTextSearch.setForeground(new java.awt.Color(0,0,0));
        }
    }

    private void jTableBooksMouseClicked(java.awt.event.MouseEvent evt) {    
        //Membuat fungsi ketika terdapat interaksi dengan table akan mengisi field yang ada sebelumnya                                     
        int line = jTableBooks.rowAtPoint(evt.getPoint()); //Deklarasi line untuk tabel

        //Mengambil nilai field dan baris yang dipilih dalam jTableBooks dan dikonversi menjadi String
        id_book = jTableBooks.getValueAt(line, 0).toString(); 
        titles = jTableBooks.getValueAt(line, 1).toString();
        author = jTableBooks.getValueAt(line, 2).toString();
        publisher = jTableBooks.getValueAt(line, 3).toString();
        release_date = jTableBooks.getValueAt(line, 4).toString();
        isbn = jTableBooks.getValueAt(line, 5).toString();

        jGetTitleField.setText(titles);
        jGetAuthorField.setText(author);
        jGetPublisherField.setText(publisher);
        jGetYearField.setText(release_date);
        jGetISBNField.setText(isbn);
    }
