        public void setTableData(){
            //Membuat objek dari class DefaultTableModel
            DefaultTableModel model = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };

            //Membuat kolom tabel
            model.addColumn("ID");
            model.addColumn("Titles");
            model.addColumn("Author");
            model.addColumn("Publisher");
            model.addColumn("Release Date");
            model.addColumn("ISBN");
    
            //Menambahkan data ke dalam baris baru pada tabel
            try {
                String sql = "SELECT * FROM book_tb";
                java.sql.Connection conn = DBcon.con;
                java.sql.Statement stm = conn.createStatement();
                java.sql.ResultSet res = stm.executeQuery(sql);

                //Mengambil record dari database dan mendapatkan nya dari book_tb
                while(res.next()){
                    model.addRow(new Object[]{
                        res.getString("id_book"),
                        res.getString("titles"),
                        res.getString("author"),
                        res.getString("publisher"),
                        res.getInt("release_date"),
                        res.getString("isbn")
                    });
                }
                jTableBooks.setModel(model);
            } catch (Exception e) {
                System.out.println("Error: " + e.getMessage());
            }
        }

